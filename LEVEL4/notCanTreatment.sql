SELECT
    A.APNT_NO,
    P.PT_NAME,
    P.PT_NO,
    D.MCDP_CD,	
    D.DR_NAME,
    A.APNT_YMD
FROM APPOINTMENT A
JOIN DOCTOR D ON A.MDDR_ID = D.DR_ID
JOIN PATIENT P ON P.PT_NO = A.PT_NO
WHERE DATE_FORMAT(A.APNT_YMD,'%Y-%M-%D') = DATE_FORMAT('2022-04-13', '%Y-%M-%D')
AND A.APNT_CNCL_YN = 'N'
AND A.MCDP_CD = 'CS'
ORDER BY A.APNT_YMD ASC 